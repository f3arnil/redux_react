var webpack = require('webpack')
var webpackDevMiddleware = require('webpack-dev-middleware')
var webpackHotMiddleware = require('webpack-hot-middleware')
var config = require('./webpack.config')

var app = new (require('express'))()
var port = 3000

var compiler = webpack(config)
app.use(webpackDevMiddleware(compiler, { noInfo: true, publicPath: config.output.publicPath }))
app.use(webpackHotMiddleware(compiler))

app.get("/static/src/styles/bootstrap.min.css", function(req, res) {
  res.sendFile(__dirname + '/src/styles/bootstrap.min.css')
})

app.get("/static/src/styles/app.css", function(req, res) {
  res.sendFile(__dirname + '/src/styles/app.css')
})

app.get("/", function(req, res) {
  res.sendFile(__dirname + '/index.html')
})

app.listen(port, function(error) {
  if (error) {
    console.error(error)
  } else {
    console.info("==> ðŸŒŽ  Listening on port %s. Open up http://localhost:%s/ in your browser.", port, port)
  }
})
